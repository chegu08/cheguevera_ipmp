class Solution{
public:
	void pushZerosToEnd(int arr[], int n) {
	    int left=0,right=0;
	    while(right<n)
	    {
	        
	        if(arr[left]==0)
	        {
	            while(arr[right]==0) right++;
	            if(right<n)
	            swap(arr[left],arr[right]);
	            
	        }
	        left++;
	            right++;
	    }
	}
};

